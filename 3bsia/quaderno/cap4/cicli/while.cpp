// Scrive i numeri da 0 a 5 utilizzando un ciclo while

#include <iostream>
using namespace std;

int main() {
    // Inizializza la variabile di controllo
    int i = 0;
    // Ciclo while per stampare i numeri da 0 a 9
    while (i < 6) {
        cout << i << endl;
        // Incrementa la variabile di controllo
        i++;
    }
    return 0;
}

// Scrive i numeri da 0 a 5 utilizzando un ciclo do/while

#include <iostream>
using namespace std;

int main() {
    // Inizializza la variabile di controllo
    int i = 0;
    // Ciclo do do/while per stampare i numeri da 0 a 9
    do {
        cout << i << endl;
        // Incrementa la variabile di controllo
        i++;
    } while (i < 6);
    return 0;
}
